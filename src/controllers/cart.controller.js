const httpStatus = require('http-status');
const { successResponse, errorResponse } = require('../utils/response.util');
const Cart = require('../models/Cart');
const Product = require('../models/Product');

/**
 * Get current user's cart
 */
const getCart = async (req, res) => {
    try {
        let cart = await Cart.findOne({ user: req.user._id }).populate('items.product');

        if (!cart) {
            cart = await Cart.create({ user: req.user._id, items: [] });
        } else {
            // Self-healing: Remove items where product is null (deleted/invalid)
            const initialCount = cart.items.length;
            cart.items = cart.items.filter(item => item.product !== null);

            if (cart.items.length !== initialCount) {
                console.log(`[Auto-Heal] Removed ${initialCount - cart.items.length} invalid items from cart ${cart._id}`);
                await cart.save();
            }
        }

        successResponse(res, httpStatus.OK, 'Cart retrieved successfully', cart);
    } catch (error) {
        console.error("GetCart Error:", error);
        errorResponse(res, httpStatus.INTERNAL_SERVER_ERROR, error.message);
    }
};

/**
 * Add item to cart
 */
const addToCart = async (req, res) => {
    try {
        const { productId, quantity, type, period } = req.body;
        const userId = req.user._id;

        // Check if product exists
        const product = await Product.findById(productId);
        if (!product) {
            return errorResponse(res, httpStatus.NOT_FOUND, 'Product not found');
        }

        let cart = await Cart.findOne({ user: userId });

        if (!cart) {
            // Create new cart
            cart = await Cart.create({
                user: userId,
                items: [{ product: productId, quantity, type, period }]
            });
        } else {
            // Check if item exists in cart
            const itemIndex = cart.items.findIndex(item =>
                item.product.toString() === productId && item.type === type
            );

            if (itemIndex > -1) {
                // Update quantity if item exists
                cart.items[itemIndex].quantity += quantity;
            } else {
                // Add new item
                cart.items.push({ product: productId, quantity, type, period });
            }
            await cart.save();
        }

        await cart.populate('items.product');

        // Self-healing check (post-populate)
        const initialCount = cart.items.length;
        cart.items = cart.items.filter(item => item.product !== null);
        if (cart.items.length !== initialCount) {
            console.log(`[Auto-Heal] Removed ${initialCount - cart.items.length} invalid items from cart ${cart._id} during add`);
            await cart.save();
        }

        successResponse(res, httpStatus.OK, 'Item added to cart', cart);
    } catch (error) {
        console.error("AddToCart Error:", error);
        errorResponse(res, httpStatus.INTERNAL_SERVER_ERROR, error.message);
    }
};

/**
 * Update cart item (quantity or period)
 */
const updateCartItem = async (req, res) => {
    try {
        const { itemId } = req.params; // Using product ID for simplicity in finding? No, itemId is better if generated by subdoc
        // Actually, frontend usually sends product ID. Let's assume URL param is ProductID for easier usage, 
        // OR we can use the `_id` of the item subdocument.
        // Let's use Product ID and Type to identify, easiest for now.
        // Wait, standard is REST. /cart/:itemId.

        const { quantity, period } = req.body;
        const userId = req.user._id;

        const cart = await Cart.findOne({ user: userId });
        if (!cart) {
            return errorResponse(res, httpStatus.NOT_FOUND, 'Cart not found');
        }

        // Find item by Reference ID (Product ID) or subdocument ID?
        // Let's search by Subdocument ID if provided, else Product ID.
        // Mongoose subdocs have _id.
        const itemIndex = cart.items.findIndex(item => item._id.toString() === itemId);

        if (itemIndex === -1) {
            return errorResponse(res, httpStatus.NOT_FOUND, 'Item not found in cart');
        }

        if (quantity !== undefined) {
            if (quantity <= 0) {
                // Remove item if quantity is 0 or less
                cart.items.splice(itemIndex, 1);
            } else {
                cart.items[itemIndex].quantity = quantity;
            }
        }

        if (period !== undefined) {
            cart.items[itemIndex].period = period;
        }

        await cart.save();
        await cart.populate('items.product');

        successResponse(res, httpStatus.OK, 'Cart updated', cart);
    } catch (error) {
        errorResponse(res, httpStatus.INTERNAL_SERVER_ERROR, error.message);
    }
};

/**
 * Remove item from cart
 */
const removeFromCart = async (req, res) => {
    try {
        const { itemId } = req.params;
        const userId = req.user._id;

        const cart = await Cart.findOne({ user: userId });
        if (!cart) {
            return errorResponse(res, httpStatus.NOT_FOUND, 'Cart not found');
        }

        cart.items = cart.items.filter(item => item._id.toString() !== itemId);

        await cart.save();
        await cart.populate('items.product');

        successResponse(res, httpStatus.OK, 'Item removed from cart', cart);
    } catch (error) {
        errorResponse(res, httpStatus.INTERNAL_SERVER_ERROR, error.message);
    }
};

/**
 * Clear cart
 */
const clearCart = async (req, res) => {
    try {
        const userId = req.user._id;
        const cart = await Cart.findOne({ user: userId });

        if (cart) {
            cart.items = [];
            await cart.save();
        }

        successResponse(res, httpStatus.OK, 'Cart cleared', { items: [] });
    } catch (error) {
        errorResponse(res, httpStatus.INTERNAL_SERVER_ERROR, error.message);
    }
};

module.exports = {
    getCart,
    addToCart,
    updateCartItem,
    removeFromCart,
    clearCart
};
